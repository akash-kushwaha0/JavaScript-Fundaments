// Double quote string
let name = "Akash"
console.log(name.length)
console.log(name[0])
console.log(name[1])

// Single quote string
let friend = 'aakash'
console.log(friend)

// Template literals
let boy1 = "Pramod"
let boy2 = "Nikhil"
// Nikhil is a friend of Pramod
let sentence = `${boy2} is a friend of ${boy1}`
console.log(sentence)

